function GM:PlayerCanHearPlayersVoice(listener, talker)
    local organism = talker.organism or {}

    if not talker:Alive() and not organism.otrub then
        return false
    end
    local distance = listener:GetPos():Distance(talker:GetPos())
    if distance > cfg.chatdist then
        return false
    end
    return true
end

function GM:PlayerCanSeePlayersChat(text, teamOnly, listener, speaker)
    local organism = speaker.organism or {}

    if not speaker:Alive() and not organism.otrub then
        return false
    end
    local distance = listener:GetPos():Distance(speaker:GetPos())
    if distance > cfg.chatdist then
        return false
    end
    return true
end


